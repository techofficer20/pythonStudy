# 파이썬에서의 덧셈
a = 1129 + 2344
print(a)  # 3473

# 파이썬에서의 뺄셈
a = 400 - 30
print(a)  # 370

# 파이썬에서의 곱셈
a = 2800 * 1.08
print(a)  # 3024.0

# 파이썬에서의 나눗셈
a = 1920 / 12
print(a)  # 160.0

# 파이썬에서의 연산 우선순위 테스트
a = (40 + 50) * 3 - 50
print(a)  # 220
a = 40 + 50 * 3 - 50
print(a)  # 140

# 파이썬에서의 나머지
a = 255 % 3
print(a)  # 0
a = 255 % 7
print(a)  # 3

# 파이썬에서의 거듭제곱
a = 2 ** 3
print(a)  # 8

# 파이썬에서의 복소수
a = (4 + 5j) - (3 - 4j)
print(a)  # 1 + 9j

# 파이썬에서의 소수점을 버린 결과
a = 1931 // 12
print(a)  # 160
